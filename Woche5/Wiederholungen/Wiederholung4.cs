using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace IL_A23_Modul_320_3.Semester.Woche5.Wiederholungen
{
    internal class Wiederholung4
    {
        static void Main()
        {
            int j = 0; // Startwert
            int zielwert = 10; // Zielwert, bis zu dem gezählt wird

            // for-Schleife: Zählt von j bis zum Zielwert
            for (int i = j; i <= zielwert; i++)
            {
                // Ausgabe des aktuellen Wertes von i
                Console.WriteLine($"i = {i}");
            }



        }
    }
}
